package model;

public class ProductVO {
	private int numA;
	private int numB;
	private int numC;
	public int getNumA() {
		return numA;
	}
	public void setNumA(int numA) {
		this.numA += numA;
	}
	public int getNumB() {
		return numB;
	}
	public void setNumB(int numB) {
		this.numB += numB;
	}
	public int getNumC() {
		return numC;
	}
	public void setNumC(int numC) {
		this.numC += numC;
	}
	
	
}
